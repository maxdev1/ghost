#include "stdio.h"
#include "stdio_internal.h"
#include "string.h"

/**
 *
 */
int fflush(FILE* stream) {

	// if no stream passed, flush all streams
	if (stream == NULL) {
		int ret = 0;

		FILE* ns = __open_file_list;
		while (ns) {
			ret |= fflush(ns);
			ns = ns->next;
		}

		return ret;
	}

	// lock file and perform flush
	g_atomic_lock(&stream->lock);
	int res = __fflush_unlocked(stream);
	stream->lock = 0;
	return res;
}
