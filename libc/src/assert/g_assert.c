#include <ghost.h>
#include "assert.h"
#include "stdlib.h"
#include "stdio.h"

/**
 *
 */
void g_assert(const char* file, int line, const char* function, const char* expr) {

	klog("Assertion failed: %s:%lu: %s: %s\n", file, line, function, expr);
	fprintf(stderr, "Assertion failed: %s:%lu: %s: %s\n", file, line, function, expr);
	exit(1);
}
