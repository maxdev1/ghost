#include "ghost/user.h"

/**
 * @see header
 */
void g_wait_for_irq(uint8_t irq) {
	g_syscall_wait_for_irq waitData;
	waitData.irq = irq;
	g_syscall(G_SYSCALL_WAIT_FOR_IRQ, (uint32_t) &waitData);
}
