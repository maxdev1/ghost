#include "ghost/user.h"

/**
 *
 */
void* g_share_mem(void* memory, int32_t size, g_pid processId) {
	g_syscall_share_mem data;
	data.memory = memory;
	data.processId = processId;
	data.size = size;
	g_syscall(G_SYSCALL_SHARE_MEMORY, (g_address) &data);
	return data.virtualAddress;
}
