#include "__internal.h"
#include "ghost/user.h"

void g_atomic_destroy(g_atom atom)
{
	g_syscall_atomic_destroy data;
	data.atom = atom;
	g_syscall(G_SYSCALL_ATOMIC_DESTROY, (g_address) &data);
}
