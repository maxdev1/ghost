#ifndef __GDT_TSS__
#define __GDT_TSS__

#include <ghost/stdint.h>

/**
 * Structure of the x86_64 task state segment.
 */
struct g_tss
{
    uint32_t reserved0;
    uint64_t rsp0;
    uint64_t rsp1;
    uint64_t rsp2;
    uint64_t reserved1;
    uint64_t ist[7];
    uint64_t reserved2;
    uint16_t reserved3;
    uint16_t iomapBase;
}__attribute__((packed));


#endif
